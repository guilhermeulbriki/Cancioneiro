    const favoritas = new Array();
    const vivencias = new Array();
    const ListarVivencias = new Array();
    const Playlists = new Array();

    for (let x = 0; x <= (titulos.length) - 1; x++) {
      musicas[x] = 'mus' + x;
      favoritas[x] = 'x';
      vivencias[x] = 'x';
    }
    window.localStorage.setItem('musicas', JSON.stringify(musicas));
    window.localStorage.setItem('titulos', JSON.stringify(titulos));
    window.localStorage.setItem('favoritas', JSON.stringify(favoritas));
